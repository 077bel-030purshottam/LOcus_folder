#Accessing string characters in Python
str = 'Hello_World'
print('str = ', str)

#first character
print('str[0] = ', str[0])

#last character
print('str[-1] = ', str[-1])

#slicing 2nd to 5th character
print('str[1:4] = ', str[1:4])

#slicing 6th to 2nd last character
print('str[5:-2] = ', str[5:-2])


#slicing from 2nd character to 6th char
print('str[1:6] = ', str[1:6])

#slicing with step
print('str[::2] = ', str[::2])

#reverse a string
print('str[::-1] = ', str[::-1])